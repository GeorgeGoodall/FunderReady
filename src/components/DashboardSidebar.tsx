export function DashboardSidebar({
  isAdmin,
  isOpen,
  onClose,
}: {
  isAdmin: boolean;
  isOpen: boolean;
  onClose: () => void;
}) {
  const pathname = usePathname();

  const items = isAdmin ? [...NAV_ITEMS, ADMIN_ITEM] : NAV_ITEMS;

  function isActive(item: NavItem) {
    return item.matchPrefixes.some((prefix) => pathname === prefix || pathname.startsWith(prefix + "/"));
  }

  const navContent = (
    <nav className="flex flex-col gap-1 px-3 py-4">
      {items.map((item) => {
        const active = isActive(item);
        return (
          <Link
            key={item.href}
            href={item.href}
            onClick={onClose}
            className={`flex items-center gap-3 rounded-lg px-3 py-2 text-sm font-medium transition-colors ${
              active
                ? "bg-zinc-100 text-zinc-900 dark:bg-zinc-800 dark:text-zinc-100"
                : "text-zinc-600 hover:bg-zinc-50 hover:text-zinc-900 dark:text-zinc-400 dark:hover:bg-zinc-800/50 dark:hover:text-zinc-100"
            }`}
          >
            {item.icon}
            {item.label}
          </Link>
        );
      })}
    </nav>
  );

  return (
    <>
      {/* Desktop sidebar — always visible */}
      {/* 57px = DashboardNav height (py-3 + content) */}
      <aside className="hidden md:block w-60 shrink-0 border-r border-zinc-200 dark:border-zinc-800 bg-white dark:bg-zinc-900 min-h-[calc(100vh-var(--nav-height,57px))]">
        {navContent}
      </aside>

      {/* Mobile overlay */}
      {isOpen && (
        <>
          <div
            className="fixed inset-0 z-40 bg-black/30 md:hidden"
            onClick={onClose}
          />
          <aside className="fixed inset-y-0 left-0 z-50 w-60 bg-white dark:bg-zinc-900 shadow-xl md:hidden pt-14">
            {navContent}
          </aside>
        </>
      )}
    </>
  );
}
